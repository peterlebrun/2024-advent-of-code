PRUNE_CONST = 0b111111111111111111111111

def iterate(s0):
        s1 = (s0^(s0<<6)) & PRUNE_CONST
        s2 = (s1^(s1>>5)) & PRUNE_CONST
        return (s2^(s2<<11)) & PRUNE_CONST

def get_sequences(init, iterations=2000):
    prices = [init % 10]
    deltas = []
    sequences = defaultdict(int)
    prev = init
    for _ in range(iterations):
        prev = iterate(prev)
        prices.append(prev % 10)
        deltas.append(prices[-1] - prices[-2])

        if len(deltas) >= 4:
            s = ",".join(map(str, deltas[-4:]))
            if s not in sequences:
                sequences[s] = prices[-1]

    return sequences

